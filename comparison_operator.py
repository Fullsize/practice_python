# 注意python 没有全等
x = 5
# python用缩进限制作用域(执行区域)
if x == 5:
    print('等于5')
    print('equal to 5')
if x > 4:
    print('大于4')
if x >= 5:
    print('大于等于5')
if x <= 5:
    print('小于等于5')
if x != 6:
    print('不等6')
